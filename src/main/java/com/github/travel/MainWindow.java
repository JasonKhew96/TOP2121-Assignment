    private void tableClickHandler(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableClickHandler
        int row = guideTable.getSelectedRow();
        if (evt.getClickCount() == 2 && row != -1) {
            String id = (String) guideTable.getModel().getValueAt(row, 0);
            Guide guide = DbHelper.getGuideById(id);
            detailsFrame.show();
            title.setText(guide.getTitle());
            intro.setText(guide.getIntro());
            contentTextPane.setText(guide.getContent());
            contentTextPane.setEditable(false);
            String path = guide.getImg();
            URL url;
            try {
                url = new URL(path);
                BufferedImage image = ImageIO.read(url);
                jLabel1.setIcon(new ImageIcon(image));
            } catch (MalformedURLException ex) {
                Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_tableClickHandler

