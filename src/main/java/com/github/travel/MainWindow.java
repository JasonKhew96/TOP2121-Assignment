    private static TableModel getGuides() {
        List<Guide> guideObj = DbHelper.getGuide();
        Object[][] guidesArr = new String[guideObj.size()][3];
        for (int i = 0; i < guideObj.size(); i++) {
            guidesArr[i][0] = Integer.toString(guideObj.get(i).getId());
            guidesArr[i][1] = guideObj.get(i).getTitle();
            guidesArr[i][2] = guideObj.get(i).getIntro();
        }
        AbstractTableModel tableModel;
        tableModel = new AbstractTableModel() {
            private String[] columnName = {"Id", "Title", "Intro"};
            private Object[][] data = guidesArr;

            @Override
            public void fireTableDataChanged() {
                super.fireTableDataChanged();
            }

            @Override
            public void setValueAt(Object aValue, int rowIndex, int columnIndex) {
                String id = (String) getValueAt(rowIndex, 0);
                Guide guide = DbHelper.getGuideById(id);
                switch (columnIndex) {
                    case 1:
                        guide.setTitle((String) aValue);
                        break;
                    case 2:
                        guide.setIntro((String) aValue);
                        break;
                }
                DbHelper.updateTable(guide);
                data[rowIndex][columnIndex] = aValue;
            }

            @Override
            public String getColumnName(int column) {
                return columnName[column];
            }

            @Override
            public int getRowCount() {
                return data.length;
            }

            @Override
            public int getColumnCount() {
                return columnName.length;
            }

            @Override
            public Object getValueAt(int rowIndex, int columnIndex) {
                return data[rowIndex][columnIndex];
            }

            @Override
            public boolean isCellEditable(int row, int col) {
                switch (col) {
                    case 0:
                        return false;
                    default:
                        return isAdmin;
                }
            }
        };
        return tableModel;
    }

